    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(evt.getSource().equals(jButton2)){
            String[] helper = jTextField1.getText().split("[ ]");
            int val = Integer.parseInt(jTextField2.getText());
            int[] arr = new int[helper.length];
            for(int i = 0; i<arr.length;i++){
                arr[i] = Integer.parseInt(helper[i]);
            }
            Arrays.sort(arr);
            long t1 = System.nanoTime();
            int ans = Searches.binSearch(arr,val);
            long t2 = System.nanoTime();
            jLabel3.setText(ans+"");
            jLabel5.setText((t2-t1)+" ns");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(evt.getSource().equals(jButton3)){
            XYSeries plot1 = new XYSeries("Linear Search");
            XYSeries plot2 = new XYSeries("Binary Search");
            for(int i = 100; i <= 2000; i+=100){
                int[] arr = new int[i*10000];
                Searches.fillArray(arr);
                long t1 = System.nanoTime();
                Searches.linSearch(arr,10);
                long t2 = System.nanoTime();
                plot1.add(arr.length,t2-t1);
                long t3 = System.nanoTime();
                Searches.binSearch(arr, 10);
                long t4 = System.nanoTime();
                plot2.add(arr.length, t4-t3);
            }
            XYSeriesCollection data = new XYSeriesCollection();
            data.addSeries(plot1);
            data.addSeries(plot2);
            JFreeChart chart = ChartFactory.createXYLineChart("Linear V. Binary Search",
                    "Array Length","Time (ns)", data,
                    PlotOrientation.VERTICAL,
                    true,true,false);
            ChartPanel panel = new ChartPanel(chart);
            JFrame plotPanel = new JFrame("Plot");
            plotPanel.setContentPane(panel);
            plotPanel.setSize(400,400);
            plotPanel.setVisible(true);
        }
    }//GEN-LAST:event_jButton3ActionPerformed


